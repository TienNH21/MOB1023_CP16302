    private void hienThiTable() {
        ArrayList<SanPham> dssp = this.qlsp.getListSP();
        
        DefaultTableModel dtm = (DefaultTableModel) this.tblSP.getModel();
        dtm.setRowCount(0);
        for (SanPham sp: dssp) {
            Object[] row = new Object[] {
                sp.getTenSP(),
                sp.getMaSP(),
                sp.getDanhMuc(),
                sp.getTinhTrang(),
                sp.getGia()
            };
            
            dtm.addRow(row);
        }
    }
    
