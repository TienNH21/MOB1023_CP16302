    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String tenSP = this.txtTenSP.getText();
        String giaSP = this.txtNgayNhap.getText();
        String maSP = this.txtMaSP.getText();
        String danhMuc = this.cbbDanhMuc.getSelectedItem().toString();
        
        int gia = Integer.parseInt(giaSP);
        
        SanPham sp = new SanPham(tenSP, maSP, danhMuc, gia, 1);
        this.qlsp.add(sp);
        this.hienThiTable();
    }//GEN-LAST:event_btnThemActionPerformed

